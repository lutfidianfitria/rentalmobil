$koneksi = new mysqli("localhost", "root", "", "rental");

// Cek koneksi
if ($koneksi->connect_error) {
    die("Koneksi gagal: " . $koneksi->connect_error);
}

// Cek apakah form disubmit
if (isset($_POST['submit'])) {
    $nopol = $_POST['nopol'];
    $brand = $_POST['brand'];
    $type = $_POST['type'];
    $tahun = $_POST['tahun'];
    $harga = $_POST['harga'];
    $status = $_POST['status'];

    // Validasi file foto
    $ekstensi_diperbolehkan = array('png', 'jpg', 'jpeg');
    $foto = $_FILES['foto']['name'];
    $x = explode('.', $foto);
    $ekstensi = strtolower(end($x));
    $ukuran = $_FILES['foto']['size'];
    $file_tmp = $_FILES['foto']['tmp_name'];

    // Cek apakah ekstensi file diperbolehkan
    if (in_array($ekstensi, $ekstensi_diperbolehkan) === true) {
        // Cek ukuran file (maksimum 1 MB)
        if ($ukuran < 1044070) {
            // Membuat direktori img jika belum ada
            if (!file_exists('img')) {
                mkdir('img', 0777, true);
            }

            // Pindahkan file ke folder img
            if (move_uploaded_file($file_tmp, 'img/' . $foto)) {
                // Insert data ke database
                $sql = "INSERT INTO tbL_mobil (nopol, brand, type, tahun, harga, status, foto) 
                        VALUES ('$nopol', '$brand', '$type', '$tahun', '$harga', '$status', '$foto')";

                // Eksekusi query
                if ($koneksi->query($sql) === TRUE) {
                    echo "<script>
                        alert('Data mobil berhasil diinput!');
                        window.location.href = 'table.php'; // Redirect setelah berhasil input
                    </script>";
                } else {
                    echo "Gagal menyimpan data: " . $koneksi->error;
                }
            } else {
                echo "Gagal upload gambar!";
            }
        } else {
            echo "Ukuran file terlalu besar, maksimal 1 MB!";
        }
    } else {
        echo "Ekstensi file tidak diperbolehkan!";
    }
}

// Tutup koneksi
